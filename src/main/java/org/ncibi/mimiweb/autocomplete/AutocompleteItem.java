package org.ncibi.mimiweb.autocomplete;

public class AutocompleteItem
{
	private Integer id ;
	private String item ;
	
	public Integer getId()
	{
		return id;
	}
	
	public void setId(Integer id)
	{
		this.id = id;
	}
	
	public String getItem()
	{
		return item;
	}
	
	public void setItem(String item)
	{
		this.item = item;
	}
}
